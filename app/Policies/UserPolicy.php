    /**
     * Determine whether the user can update the model.
     * Any user can update his own account.
     * Or user must be a member of account where user is an owner.
     * Or user is admin.
     *
     * @param  \App\User  $user
     * @param  \App\User  $model
     * @return mixed
     */
    public function update(User $user, User $model)
    {
        if (
            $user->id == $model->id
            || optional($model->memberOfAccount)->owner_id == $user->id
            || $user->isAdmin()
        )
            return true;
    }

